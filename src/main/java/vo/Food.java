package vo;

public class Food {
	
	private String warecode;
	private String warename;
	private String usecheck;
	private String warelike;
	private String waresearch;
	public Food(String warecode, String warename, String usecheck, String warelike, String waresearch) {
		super();
		this.warecode = warecode;
		this.warename = warename;
		this.usecheck = usecheck;
		this.warelike = warelike;
		this.waresearch = waresearch;
	}
	public String getWarecode() {
		return warecode;
	}
	public void setWarecode(String warecode) {
		this.warecode = warecode;
	}
	public String getWarename() {
		return warename;
	}
	public void setWarename(String warename) {
		this.warename = warename;
	}
	public String getUsecheck() {
		return usecheck;
	}
	public void setUsecheck(String usecheck) {
		this.usecheck = usecheck;
	}
	public String getWarelike() {
		return warelike;
	}
	public void setWarelike(String warelike) {
		this.warelike = warelike;
	}
	public String getWaresearch() {
		return waresearch;
	}
	public void setWaresearch(String waresearch) {
		this.waresearch = waresearch;
	}
	
	
	
	
	
	



	

	
	
	
		
}
